#│            │  Лучший случай │  Средний случай  │  Худший случай │
#├────────────┼────────────────┼──────────────────┼────────────────┤
#│ **Время**  │     O(n)       │      O(n²)       │     O(n²)      │
#├────────────┼────────────────┼──────────────────┼────────────────┤
#│ **Память** │     O(1)       │      O(1)        │     O(1)       │
#└────────────┴────────────────┴──────────────────┴────────────────┘

def InsertionSort(arr):
    for i in range(len(arr)):
        j = i - 1
        while j >= 0 and arr[j] > arr[j + 1]:
            arr[j], arr[j + 1] = arr[j+1], arr[j]
            j -= 1

    return arr

print(InsertionSort([2,3,4,1,2,3]))